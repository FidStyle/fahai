<p>1、问：关于为何替换产品中显瞩的链接及破解补丁为何需要加壳等原因？<br />
答：主要在于目前有很多网友对其破解的补丁版权进行修改并在互联网中出售（严厉谴责倒卖人员），为防止该类事件不得已只能对显瞩位置的文字替换其网址及对破解补丁进行加壳操作等方式。</p>
<p>2、问：关于为何存在获取计算机相关信息及杀毒软件报毒等原因？<br />
答：目前通过检查得知获取计算机相关信息应是使用了VMP加密壳所带来的问题，其拥有授权管理的功能，所以获取硬件信息是必要的。杀毒软件报毒主要在于其破解模式采用了DLL劫持的方法，具体劫持原理可通过百度、谷歌搜索了解，误报是非常正常的。</p>
<p>3、问：通过某平台检测其补丁存在外连行为？<br />
答：根据网友提供的检测平台结果得知其补丁似乎外连了一个IP为104.111.168.50的地址，通过对其平台反复测试结果得知，只要对劫持补丁进行加壳操作，报告中均会存在该外连IP，可自行本地验证下是否真实存在该外连IP。</p>
